			<h1>Iconic Design in Sci-Fi Movies</h1>
		</div>

		<div class="entry">
     <a href="http://sijobling.com/portfolio/iconic-designs-in-sci-fi-movies/" rel="bookmark" title="Iconic Design in Sci-Fi Movies"><img width="490" height="200" src="http://sijobling.com/wp-content/uploads/2012/04/minority_report_user_interface-490x200.jpg" class="attachment-post-thumbnail wp-post-image" alt="minority_report_user_interface" title="minority_report_user_interface" /></a>
			<p>Recently, I was asked my thoughts on iconic design in sci-fi movies by online magazine, <a href="http://www.creativebloq.com/">Creative BloQ</a>, specifically about the user interface from <a href="http://www.imdb.com/title/tt0181689/">Minority Report</a>:</p>
			<blockquote><p>The user interface from Minority Report was scarily close to what is now possible, hence why it’s been such an iconic design in film. The motion gestures on a projected screen seemed so far-fetched in 2002 but, less than ten years later, Microsoft had mass-produced the Xbox Kinect device simulating exactly the same concept. Who knew?</p></blockquote>
			<p><a href="http://www.creativebloq.com/digital-art/20-best-designs-in-sci-fi-movies-1233236/">Read the full article</a> for 19 other interesting thoughts on the topic.</p>
			<p>As part of the process, I also considered a number of other iconic designs from sci-fi movies;</p>
			<p>How can you talk about design in sci-fi without referencing <a href="http://www.imdb.com/title/tt0133093/">The Matrix (1999)</a>? The &#8220;bullet time&#8221; camera effect introduced by the Wachowski brothers inspired many following movies to adopt the same technique. The vertically scrolling matrix user interface also touched a nerve for many geeks and will always be considered the ultimate computer interface.</p>
			<p><a href="http://www.imdb.com/title/tt0091059/">Flight of the Navigator (1986)</a> left a lasting impression for me with the friendly, robotic eye of Max &#8211; an alternative alien concept with a friendly character, relatively unheard of up to then &#8211; and the streamline, curved shape of the spaceship where most UFO designs up to this time went with the traditional disc shape. The controls for flying the ship could also be considered to inspire some of the gaming controls we&#8217;re now familiar with today.</p>
			<p>Can you think of any other iconic designs from sci-fi?</p>
